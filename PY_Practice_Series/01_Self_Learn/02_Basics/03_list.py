# Define a list called mylist
mylist = [123, "janak", 3.14]

# Print the list
print(mylist)  # Output: [123, 'janak', 3.14]

# Get the length of the list
print(len(mylist))  # Output: 3

# Access the first element of the list
print(mylist[0])  # Output: 123

# Access the last element of the list
print(mylist[-1])  # Output: 3.14

# Access a slice of the list from index 1 to index 2 (exclusive)
print(mylist[1:2])  # Output: ['janak']

# List available attributes and methods of the list
print(dir(mylist))
