# Define a tuple named myTup
myTup = (1, 2, 3, 4, 5, 6)

# Print the tuple
print(myTup)  # Output: (1, 2, 3, 4, 5, 6)

# Access the first element of the tuple
print(myTup[0])  # Output: 1

# Access the last element of the tuple
print(myTup[-1])  # Output: 6

# Access a slice of the tuple from index 1 to index 3 (exclusive)
print(myTup[1:4])  # Output: (2, 3, 4)

# Get the length of the tuple
print(len(myTup))  # Output: 6
